ALL_PI = {**P2, **P3, **P4, **P5, **P6, **P7, **P8, **P9_EXTRA}


def get_image(name, N, _cache={}):
    key = (name, N)
    if key in _cache:
        return _cache[key]
    spec = ALL_PI[name]
    aii_pts = enumerate_aii_n3_full(N)
    image = set()
    bad = 0
    for p in aii_pts:
        q = apply_pi(spec, p)
        ok, reason = bdi_feasible_n3(q)
        if not ok:
            bad += 1
            continue
        image.add((q["M_1"], q["M_2"], q["B_1"], q["T_1"],
                   q["B_2"], q["T_2"], q["S"]))
    _cache[key] = (image, bad, len(aii_pts))
    return image, bad, len(aii_pts)


def main():
    import time
    print(f"=== v9 verifier: {len(ALL_PI)} pieces total ({len(P9_EXTRA)} new in v9) ===")
    pieces = list(ALL_PI.keys())

    # Check land-in-cone on small N first to catch design errors
    print("Land-in-cone check for new pieces at N=4:")
    for name in P9_EXTRA:
        _, bad, n_aii = get_image(name, 4)
        if bad > 0:
            print(f"  WARNING: {name} has {bad}/{n_aii} land-in-cone failures")
    print()

    Ns = [11, 12, 13, 14, 15]
    print(f"{'N':>3} | {'|BDI|':>7} | {'covered':>8} | {'coverage':>10} | {'time (s)':>9}")
    print("-" * 60)
    for N in Ns:
        t0 = time.time()
        bdi_pts = enumerate_bdi_n3(N)
        union = set()
        for name in pieces:
            img, _, _ = get_image(name, N)
            union |= img
        cov = len(union & bdi_pts)
        dt = time.time() - t0
        pct = 100.0 * cov / len(bdi_pts) if bdi_pts else 0
        print(f"{N:>3} | {len(bdi_pts):>7} | {cov:>8} | {pct:>9.2f}% | {dt:>9.2f}")
        if cov < len(bdi_pts):
            missing = bdi_pts - union
            print(f"    MISSING at N={N}: {len(missing)} points (showing first 30):")
            for q in sorted(missing)[:30]:
                _, M_2, B_1, T_1, B_2, T_2, S = q
                P_1 = 2*(B_1-T_1); P_2 = P_1 + 2*(B_2-T_2)
                print(f"      M2={M_2} B1={B_1} T1={T_1} B2={B_2} T2={T_2} S={S}  "
                      f"P_1={P_1} P_2={P_2}")


if __name__ == "__main__":
    main()
