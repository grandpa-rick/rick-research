"""
Compute the change-of-basis matrix M: F -> F' between two LMNP-style
Verma bases of L(lambda) for sp_4, where:

    F  = { f_1^{a_4} f_2^{a_3} f_1^{a_2} f_2^{a_1} v_lambda } (Cao-Huang, w_0 = r_1 r_2 r_1 r_2)
    F' = { f_2^{a'_4} f_1^{a'_3} f_2^{a'_2} f_1^{a'_1} v_lambda } (w_0' = r_2 r_1 r_2 r_1)

Both are 40-element bases of L(omega_1 + 2 omega_2) = L(3 e_1 + 2 e_2) for sp_4 = C_2.

Strategy:
  1. PBW basis of U(n^-)(sp_4): {f_1^{c_1} f_{112}^{c_2} f_{12}^{c_3} f_2^{c_4}}
       where f_{12} = [f_1, f_2], f_{112} = [f_1, f_{12}].
     Commutation relations:
       [f_1, f_{112}] = 0  (Serre: ad(f_1)^3 f_2 = 0)
       [f_{112}, anything] = 0
       [f_{12}, f_1] = -f_{112}  (so f_{12} f_1 = f_1 f_{12} - f_{112})
       [f_{12}, f_2] = 0  (Serre: ad(f_2)^2 f_1 = 0)
       [f_2, f_1] = -f_{12}  (so f_2 f_1 = f_1 f_2 - f_{12})

  2. L(lambda) = U(n^-) / J where J = U(n^-) f_1^{m_1+1} + U(n^-) f_2^{m_2+1}
     (BGG: integral dominant lambda has max submodule of M(lambda) generated by
      the simple-root singular vectors).
     For lambda = omega_1 + 2 omega_2: m_1=1, m_2=2; J = U f_1^2 + U f_2^3.

  3. Build U(n^-)_mu in PBW basis for each weight mu = n_1 alpha_1 + n_2 alpha_2.
     Compute the subspace J_mu spanned by {w f_1^2 : w in U_{mu - 2 alpha_1}}
     and {w f_2^3 : w in U_{mu - 3 alpha_2}}. Then L(lambda)_mu = U_mu / J_mu.

  4. Expand each F-basis vector u_a and each F'-basis vector u'_a' in PBW
     basis. Each lives in U(n^-)_mu for appropriate mu.

  5. Solve M_{a', a} satisfying u'_{a'} = sum_a M_{a', a} u_a (mod J_mu),
     using linear algebra over Q.

  6. Analyze support, classify by net-move type Delta = a' - a (or similar).
"""

from fractions import Fraction
from collections import defaultdict
from itertools import product
from math import comb


# ---------------------------------------------------------------------
# PBW arithmetic
# ---------------------------------------------------------------------
# PBW monomial: tuple (c_1, c_2, c_3, c_4) = exponents of (f_1, f_112, f_12, f_2).
# PBW element: dict mapping tuple -> Fraction.

def zero():
    return {}


def clean(d):
    return {k: v for k, v in d.items() if v != 0}


def add(*elts):
    out = defaultdict(Fraction)
    for e in elts:
        for k, v in e.items():
            out[k] += v
    return clean(dict(out))


def scale(elt, c):
    if c == 0:
        return {}
    return {k: v * c for k, v in elt.items()}


def neg(elt):
    return {k: -v for k, v in elt.items()}


def mon(c1, c2, c3, c4, coef=1):
    if coef == 0:
        return {}
    return {(c1, c2, c3, c4): Fraction(coef)}


def left_mul_f1(elt):
    """Multiply elt by f_1 on the left.  f_1 . (f_1^{c1} f_112^{c2} f_12^{c3} f_2^{c4}) = (c1+1, c2, c3, c4)."""
    out = defaultdict(Fraction)
    for (c1, c2, c3, c4), v in elt.items():
        out[(c1 + 1, c2, c3, c4)] += v
    return clean(dict(out))


def left_mul_f2(elt):
    """Multiply elt by f_2 on the left.

    f_2 . f_1^{c1} f_112^{c2} f_12^{c3} f_2^{c4}
      = f_1^{c1} f_112^{c2} f_12^{c3} f_2^{c4+1}
        - c1 * f_1^{c1-1} f_112^{c2} f_12^{c3+1} f_2^{c4}
        + C(c1, 2) * f_1^{c1-2} f_112^{c2+1} f_12^{c3} f_2^{c4}.

    Derived from:
        [f_1, f_2] = f_12,  [f_1, f_12] = f_112,  [f_1, f_112] = 0,
        f_2 commutes with f_12 and f_112,
        f_12 commutes with f_112.
    Then f_2 f_1^c = f_1^c f_2 - c f_1^{c-1} f_12 + C(c,2) f_1^{c-2} f_112.
    """
    out = defaultdict(Fraction)
    for (c1, c2, c3, c4), v in elt.items():
        out[(c1, c2, c3, c4 + 1)] += v
        if c1 >= 1:
            out[(c1 - 1, c2, c3 + 1, c4)] += -c1 * v
        if c1 >= 2:
            out[(c1 - 2, c2 + 1, c3, c4)] += Fraction(comb(c1, 2)) * v
    return clean(dict(out))


def right_mul_pbw_mon(elt, e1, e2, e3, e4):
    """Multiply elt by the PBW monomial (e1, e2, e3, e4) on the RIGHT.

    This is needed for building generators of J = U f_1^2 + U f_2^3:
    we need {w * f_1^2} for arbitrary w, which is right-multiplication of w by f_1^2.

    Right-multiplication by f_1: an PBW monomial f_1^{c1} f_112^{c2} f_12^{c3} f_2^{c4}
    times f_1 on the right. We need to commute f_1 leftward past f_2^{c4}, then f_12^{c3},
    then f_112^{c2}, into f_1^{c1+...}.
    """
    # We compute by repeatedly applying right_mul_f1 / right_mul_f2.
    cur = elt
    for _ in range(e1):
        cur = right_mul_f1(cur)
    for _ in range(e2):
        cur = right_mul_f112(cur)
    for _ in range(e3):
        cur = right_mul_f12(cur)
    for _ in range(e4):
        cur = right_mul_f2(cur)
    return cur


def right_mul_f1(elt):
    """Multiply elt by f_1 on the right.

    f_1^{c1} f_112^{c2} f_12^{c3} f_2^{c4} . f_1
    = f_1^{c1} f_112^{c2} f_12^{c3} (f_2^{c4} f_1).
    We need f_2^{c4} f_1 in PBW form: use f_2^k f_1 iteratively.

    f_2 f_1 = f_1 f_2 - f_12
    f_2^2 f_1 = f_2 (f_2 f_1) = f_2 (f_1 f_2 - f_12) = (f_2 f_1) f_2 - f_2 f_12
              = (f_1 f_2 - f_12) f_2 - f_12 f_2
              = f_1 f_2^2 - 2 f_12 f_2
    f_2^k f_1 = f_1 f_2^k - k f_12 f_2^{k-1}   (general, since [f_2, f_12]=0 simplifies)

    Then  (f_12^{c3}) . (f_1 f_2^k - k f_12 f_2^{k-1})
        = f_12^{c3} f_1 f_2^k - k f_12^{c3+1} f_2^{k-1}.
    Use f_12^{c3} f_1 in PBW:
        f_12 f_1 = f_1 f_12 - f_112
        f_12^j f_1 = f_1 f_12^j - j f_112 f_12^{j-1}  (since [f_1, f_112]=0)
    """
    out = defaultdict(Fraction)
    for (c1, c2, c3, c4), v in elt.items():
        # Step 1:  f_2^{c4} . f_1 = f_1 f_2^{c4} - c4 f_12 f_2^{c4 - 1}
        # (uses [f_2, f_12] = 0)
        # so f_12^{c3} f_2^{c4} f_1 = f_12^{c3} (f_1 f_2^{c4} - c4 f_12 f_2^{c4-1})
        # Step 2: simplify f_12^{c3} f_1 = f_1 f_12^{c3} - c3 f_112 f_12^{c3-1}
        #
        # Two terms after step 1:
        # term A: f_1^{c1} f_112^{c2} f_12^{c3} f_1 f_2^{c4}
        #       = f_1^{c1} f_112^{c2} (f_1 f_12^{c3} - c3 f_112 f_12^{c3-1}) f_2^{c4}
        #       = f_1^{c1+1} f_112^{c2} f_12^{c3} f_2^{c4}    -- since f_1 f_112^{c2} = f_112^{c2} f_1
        #         - c3 f_1^{c1} f_112^{c2+1} f_12^{c3-1} f_2^{c4}
        # term B: -c4 * f_1^{c1} f_112^{c2} f_12^{c3+1} f_2^{c4-1}
        out[(c1 + 1, c2, c3, c4)] += v
        if c3 >= 1:
            out[(c1, c2 + 1, c3 - 1, c4)] += -c3 * v
        if c4 >= 1:
            out[(c1, c2, c3 + 1, c4 - 1)] += -c4 * v
    return clean(dict(out))


def right_mul_f2(elt):
    """Multiply elt by f_2 on the right.  Since f_2 commutes with f_12, f_112, and itself,
    this just increments c4."""
    out = defaultdict(Fraction)
    for (c1, c2, c3, c4), v in elt.items():
        out[(c1, c2, c3, c4 + 1)] += v
    return clean(dict(out))


def right_mul_f12(elt):
    """Right-mult by f_12.

    f_1^{c1} f_112^{c2} f_12^{c3} f_2^{c4} . f_12
    = f_1^{c1} f_112^{c2} f_12^{c3} f_12 f_2^{c4}    (f_2 and f_12 commute)
    = f_1^{c1} f_112^{c2} f_12^{c3+1} f_2^{c4}.
    """
    out = defaultdict(Fraction)
    for (c1, c2, c3, c4), v in elt.items():
        out[(c1, c2, c3 + 1, c4)] += v
    return clean(dict(out))


def right_mul_f112(elt):
    """Right-mult by f_112.  f_112 commutes with everything, so just increment c2."""
    out = defaultdict(Fraction)
    for (c1, c2, c3, c4), v in elt.items():
        out[(c1, c2 + 1, c3, c4)] += v
    return clean(dict(out))


# ---------------------------------------------------------------------
# Sanity tests for arithmetic
# ---------------------------------------------------------------------

def test_arithmetic():
    # f_2 f_1 = f_1 f_2 - f_12
    a = mon(0, 0, 0, 0)
    a = left_mul_f1(a)            # f_1
    a = left_mul_f2(a)            # f_2 f_1
    expected = add(mon(1, 0, 0, 1), mon(0, 0, 1, 0, coef=-1))
    assert a == expected, (a, expected)

    # f_2 f_1^2 = f_1^2 f_2 - 2 f_1 f_12 + f_112
    a = mon(0, 0, 0, 0)
    a = left_mul_f1(a)
    a = left_mul_f1(a)
    a = left_mul_f2(a)
    expected = add(mon(2, 0, 0, 1), mon(1, 0, 1, 0, coef=-2), mon(0, 1, 0, 0, coef=1))
    assert a == expected, (a, expected)

    # f_2 f_1^3 = f_1^3 f_2 - 3 f_1^2 f_12 + 3 f_1 f_112
    a = mon(0, 0, 0, 0)
    a = left_mul_f1(a)
    a = left_mul_f1(a)
    a = left_mul_f1(a)
    a = left_mul_f2(a)
    expected = add(mon(3, 0, 0, 1), mon(2, 0, 1, 0, coef=-3), mon(1, 1, 0, 0, coef=3))
    assert a == expected, (a, expected)

    # right_mul_f1(f_2) should give f_2 f_1 = f_1 f_2 - f_12
    a = mon(0, 0, 0, 1)
    a = right_mul_f1(a)
    expected = add(mon(1, 0, 0, 1), mon(0, 0, 1, 0, coef=-1))
    assert a == expected, (a, expected)

    # right_mul_f1 of f_12 should give f_12 f_1 = f_1 f_12 - f_112
    a = mon(0, 0, 1, 0)
    a = right_mul_f1(a)
    expected = add(mon(1, 0, 1, 0), mon(0, 1, 0, 0, coef=-1))
    assert a == expected, (a, expected)

    # f_2 f_12 should equal f_12 f_2 (commute)
    a1 = mon(0, 0, 1, 0)
    a1 = left_mul_f2(a1)
    a2 = mon(0, 0, 0, 1)
    a2 = right_mul_f12(a2)
    assert a1 == a2, (a1, a2)

    print("Arithmetic tests passed.")


